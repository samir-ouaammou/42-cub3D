#include "ft_printf.h"

int	ft_putstr(char *s)
{
	int	i;

	i = 0;
	if (!s)
		return (write(2, "(null)", 6));
	while (s[i])
	{
		write(2, &s[i], 1);
		i++;
	}
	return (i);
}
